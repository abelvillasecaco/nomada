// Dado un array con tres números, usa destructuración para extraerlos en variables individuales.

const numeros = [10, 20, 30];

// Destructuración del array
const [primero, segundo, tercero] = numeros;

console.log(primero); // 10
console.log(segundo); // 20
console.log(tercero); // 30

// Se extraen los valores del array en variables individuales.